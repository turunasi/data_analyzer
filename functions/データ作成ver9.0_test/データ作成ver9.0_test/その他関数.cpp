//標準ライブラリのインクルード
#include<stdio.h>
#include<math.h>

//------------------------------------------------------------------------------------------------------------------------------------------
//log2(x)を求める関数（繰り上げ整数）
//------------------------------------------------------------------------------------------------------------------------------------------
unsigned int Log2( unsigned int x ){
	
	//変数宣言---------------------------------------------------------------------------------------
	unsigned int xx	= x-1;
	unsigned int y	= 0;

	//処理-------------------------------------------------------------------------------------------
	while(1){
		xx >>= 1;
		y++;
		if( xx==0 ){
			break;
		}
	}
	return y;
}


//------------------------------------------------------------------------------------------------------------------------------------------
//pow2(x)を求める関数
//------------------------------------------------------------------------------------------------------------------------------------------
unsigned int Pow2( unsigned int x ){
	//処理-------------------------------------------------------------------------------------------
	return (1<<x);
}

